#include<stdio.h>
int main(){
	//while悜遠 
	int a=0;
	while(a<1){
		printf("*");
		a++;
	}
	printf("\n");
	
	//for悜遠 
	for(int b=0;b<2;b++){
		printf("*");
	}
	printf("\n");

	//for悜遠 
	for(int b=5;b>=3;b--){
	printf("*");	
	}
	printf("\n");



	//do-while悜遠 
	int b=0; 
	do{
		printf("*");
		b++;
		
	}while(b<4);
	
	
} 
